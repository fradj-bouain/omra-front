@Injectable({ providedIn: 'root' })
export class I18nService {
  private readonly http = inject(HttpClient);
  private readonly api = inject(ApiService);

  private readonly dict = signal<Record<string, string>>({});
  readonly loaded = signal(false);
  readonly currentLang = signal<UiLang>('fr');

  /** Pour alignement futur (dates, nombres). */
  readonly angularLocaleId = computed(() => {
    const l = this.currentLang();
    if (l === 'ar') return 'ar';
    if (l === 'en') return 'en-US';
    return 'fr-FR';
  });

  async initialize(): Promise<void> {
    const stored = localStorage.getItem(STORAGE_KEY) as UiLang | null;
    const lang: UiLang = stored === 'ar' ? 'ar' : stored === 'en' ? 'en' : 'fr';
    this.currentLang.set(lang);
    await this.loadLocale(lang, true);
  }

  async loadLocale(lang: UiLang, isInitial = false): Promise<void> {
    try {
      const map = await firstValueFrom(
        this.http.get<Record<string, string>>(this.api.i18n.translations(lang))
      );
      this.dict.set(map && typeof map === 'object' ? map : {});
    } catch {
      if (isInitial && lang === 'fr') this.dict.set({ ...FALLBACK_FR });
      else if (isInitial && lang === 'en') this.dict.set({ ...FALLBACK_EN });
      else if (isInitial && lang === 'ar') this.dict.set({ ...FALLBACK_AR });
      else this.dict.set({});
    }
    this.applyDocumentLang(lang);
    this.loaded.set(true);
  }

  setLanguage(lang: UiLang): void {
    localStorage.setItem(STORAGE_KEY, lang);
    this.currentLang.set(lang);
    void this.loadLocale(lang, false);
  }

  instant(key: string, params?: Record<string, string | number>): string {
    let s: string | undefined;
    if (this.currentLang() === 'fr' && Object.prototype.hasOwnProperty.call(FALLBACK_FR, key)) {
      s = FALLBACK_FR[key];
    } else if (
      this.currentLang() === 'ar' &&
      Object.prototype.hasOwnProperty.call(FALLBACK_AR, key)
    ) {
      s = FALLBACK_AR[key];
    } else if (
      this.currentLang() === 'en' &&
      Object.prototype.hasOwnProperty.call(FALLBACK_EN, key)
    ) {
      s = FALLBACK_EN[key];
    } else {
      s = this.dict()[key];
      if (s == null || s === '') {
        s = FALLBACK_AR[key] ?? FALLBACK_EN[key] ?? FALLBACK_FR[key] ?? key;
      }
    }
    if (params) {
      for (const [k, v] of Object.entries(params)) {
        s = s.split(`{{${k}}}`).join(String(v));
      }
    }
    return s;
  }

  private applyDocumentLang(lang: UiLang): void {
    if (typeof document === 'undefined') return;
    document.documentElement.lang = lang === 'ar' ? 'ar' : lang === 'en' ? 'en' : 'fr';
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  }
}
